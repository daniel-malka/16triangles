function setup() {
  createCanvas(windowWidth, windowHeight);
  background(0);
}
function draw() {
  noLoop();
  noStroke();

  for (let y = 40; y < height - 40; y += 40) {
    for (let x = 40; x < width - 40; x += 40) {
      let randIndex = floor(random(0, trianglesConfigs.length));
      let triangles = trianglesConfigs[randIndex].triangles;

      fill(255);
      strokeWeight(5);

      for (let i = 0; i < triangles.length; i++) {
        let triangleNum = triangles[i];
        drawTri16(x, y, triangleNum);
      }
    }
  }
}
